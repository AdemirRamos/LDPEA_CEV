#Operadores Lógicos e Ralacionais:

#>: maior que; <: menor que; >=: maior ou igual a; <=: menor igual a; =: igual a; <>: diferente de.

#Operadores Lógicos:

#"E", "Ou" e "NÃO".

#"NÃO" inverte o valor "boolean".

#IMPORTANTE: Comparações relacionais devem ir entre parênteses - pois só assim elas geram resultados lógicos.

#Ordem de Precedência Geral: 1ª: operadores aritméticos; 2ª: operadores relacionais; 3ª: operações lógicas (ordem: "E", "OU", "NÃO").

#P. S.: Operadores relacionais são resolvidos na ordem em que aparecem.

